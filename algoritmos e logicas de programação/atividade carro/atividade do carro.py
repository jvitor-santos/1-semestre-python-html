tempo =int(input("Digite o tempo gasto em horas"))
velocidade =int(input("Digite a velocidade média em Km "))
distancia=(tempo*velocidade)
litros=(distancia)/12
print("A distancia percorrida é de ", distancia,"Km")
print("A quantidade de gasolina gasta é de ", litros,"L")
print("A velocidade médeia foi de ", velocidade,"Km/h")
print("O tempo foi de ", tempo,"h")
